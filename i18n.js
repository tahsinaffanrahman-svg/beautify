    /* ------------------------------------------------------------------
       Core translation function
       ------------------------------------------------------------------ */
    function t(key, vars) {
        if (vars === undefined) vars = {};
        let val = _lookup(key, currentLang);
        if (val === undefined) val = _lookup(key, 'en');
        if (val === undefined) val = key;
        return val.replace(/\{([^}]+)\}/g, function (_, name) {
            return vars[name] !== undefined ? vars[name] : '{' + name + '}';
        });
    }

    function _lookup(key, lang) {
        const dict = translations[lang];
        return dict ? dict[key] : undefined;
    }

    /* ------------------------------------------------------------------
       Set language and persist
       ------------------------------------------------------------------ */
    function setLang(lang) {
        if (!translations[lang]) return;
        currentLang = lang;
        try { localStorage.setItem(STORAGE_KEY, lang); } catch (e) {}
        _translateDOM();
        var ev = new CustomEvent('languagechange', { detail: { lang: lang } });
        document.dispatchEvent(ev);
        for (var i = 0; i < listeners.length; i++) listeners[i](lang);
    }

    function getLang() { return currentLang; }

    function onLangChange(fn) { listeners.push(fn); }

    /* ------------------------------------------------------------------
       Translate DOM elements with data-i18n / data-i18n-html
       ------------------------------------------------------------------ */
    function _translateDOM() {
        var els = document.querySelectorAll('[data-i18n]');
        for (var i = 0; i < els.length; i++) {
            var el = els[i];
            var key = el.getAttribute('data-i18n');
            var val = t(key);
            if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
                el.placeholder = val;
            } else {
                el.textContent = val;
            }
        }
        var htmlEls = document.querySelectorAll('[data-i18n-html]');
        for (var j = 0; j < htmlEls.length; j++) {
            var el2 = htmlEls[j];
            el2.innerHTML = t(el2.getAttribute('data-i18n-html'));
        }
        var ariaEls = document.querySelectorAll('[data-i18n-aria-label]');
        for (var k = 0; k < ariaEls.length; k++) {
            ariaEls[k].setAttribute('aria-label', t(ariaEls[k].getAttribute('data-i18n-aria-label')));
        }
        var titleEls = document.querySelectorAll('[data-i18n-title]');
        for (var l = 0; l < titleEls.length; l++) {
            titleEls[l].setAttribute('title', t(titleEls[l].getAttribute('data-i18n-title')));
        }
        var valueEls = document.querySelectorAll('[data-i18n-value]');
        for (var m = 0; m < valueEls.length; m++) {
            valueEls[m].setAttribute('value', t(valueEls[m].getAttribute('data-i18n-value')));
        }
    }

    /* ------------------------------------------------------------------
       Init: detect language, translate DOM
       ------------------------------------------------------------------ */
    function init() {
        var stored = null;
        try { stored = localStorage.getItem(STORAGE_KEY); } catch (e) {}
        var detected = stored || navigator.language || navigator.userLanguage || DEFAULT_LANG;
        detected = detected.substring(0, 2);
        if (!translations[detected]) detected = DEFAULT_LANG;
        setLang(detected);
    }

    /* ------------------------------------------------------------------
       Expose public API
       ------------------------------------------------------------------ */
    window.i18n = {
        t: t,
        setLang: setLang,
        getLang: getLang,
        onLangChange: onLangChange,
        init: init,
        langNames: langNames,
        available: Object.keys(translations),
    };
